package basic;

public class productInfo {
	private String name; //"초코케이크3호"
	private int value;//20000
	
	public productInfo() {
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
	
}
